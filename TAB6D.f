      SUBROUTINE TAB6D(API60,DEGF,VCFC,VCFP,IFLAG)
      
      double precision API60, DEGF, VCFC,VCFP
      integer IHYDRO, IFLAG

      DATA IBAS /600/
      DATA NBP1 /450/
      DATA ITMP1/3000/
      DATA K0,K1/0,34878/

      VCFC=-1.0000
      VCFP=-1.0000
      IFLAG=-1
      
      RI=0.5
      IF(API60)5,6,6
 5    RI=-0.5
 6    IAPI=api60*10.0 + RI
      API60=FLOAT(IAPI) / 10.0
      ITEMP=DEGF*10.0 + 0.5
      DEGF=FLOAT(ITEMP)/10.0
      
      IF(IAPI+100)10,20,20
 10   CONTINUE
      RETURN
 20   IF(IAPI-NBP1)30,30,10
 30   CONTINUE
      
      IDT=ITEMP-IBAS
      IF(ITEMP)40,60,60
      
 40   CONTINUE
      IFLAG=-1
      RETURN
 60   IF(ITEMP-ITMP1)100,100,40
 100  CONTINUE
      
      CALL RHO6D(IAPI,IRHO)
      
      CALL ALFA6D(IRHO, K0, K1, IALF)
      
      CALL VCF6D(IALF, IDT, IVCF)
      IFLAG=0
      
      
      JVCF=(IVCF/1000+5)/10
      PVCF=JVCF
      PVCF=PVCF/10000.
      IF(IVCF-100000000)230,240,240
 230  CONTINUE
      
      JVCF=(IVCF/100+5)/10
      CVCF=JVCF
      CVCF=CVCF/100000.
      GO TO 250
 240  CONTINUE
      CVCF=PVCF
 250  CONTINUE
      VCFP=PVCF
      VCFC=CVCF
      RETURN
      END
      

      







      
      SUBROUTINE RHO6D(IAPI,IRHO)
      
      IDENOM=IAPI+1315
      IRHO=(1413601980/IDENOM+5)/10
      RETURN
      END
      
      SUBROUTINE DIV6D(INUM,IDENOM,IRES,ISCALE)
      
      
      IRES1=INUM/IDENOM
      IRES2=(INUM-IRES1*IDENOM)*ISCALE/IDENOM
      
      IRES=IRES1*ISCALE+IRES2
      RETURN
      END
      
      SUBROUTINE ALFA6D(IRHO,K0,K1,IALF)
      INUM=K1*1000
      CALL DIV6D(INUM,IRHO,IALF1,10000)
      
      INUM=K0*100
      CALL DIV6D(INUM,IRHO,IALFS,10000)
      CALL DIV6D(IALFS,IRHO,IALF2,10000)
      IALF=(IALF1+IALF2+500)/1000
      RETURN
      END
      
      SUBROUTINE VCF6D(IALF,IDT,IVCF)
      
      ITERM1=IALF*IDT
      ITERM2=ITERM1/5*4
      
      CALL MPY6D(ITERM1,ITERM2,ITERM3,10000)
      IX=-ITERM1-ITERM3
      
      ISUM1=100000000+IX
      CALL MPY6D(IX,IX,ISUM2,10000)
      ISUM2=ISUM2/2
      CALL MPY6D(IX,ISUM2,ISUM3,10000)
      ISUM3=ISUM3/3
      CALL MPY6D(IX,ISUM3,ISUM4,10000)
      ISUM4=ISUM4/4
      CALL MPY6D(IX,ISUM4,ISUM5,10000)
      ISUM5=ISUM5/5
      CALL MPY6D(IX,ISUM5,ISUM6,10000)
      ISUM6=ISUM6/6
      
      IVCF=ISUM1+ISUM2+ISUM3+ISUM4+ISUM5+ISUM6
      RETURN
      END
      
      
      SUBROUTINE MPY6D(IX,IY,IZ,ISCALE)
      
      
      IU1=IX/ISCALE
      K1=ISCALE*IU1
      IV1=IX-K1
      IU2=IY/ISCALE
      K2=ISCALE*IU2
      IV2=IY-K2
      K3=IU1*IV2+IU2*IV1+IV1*IV2/ISCALE
      IZ=(K3+ISCALE/2)/ISCALE + IU1*IU2
      RETURN
      END
      

      
