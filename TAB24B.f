      SUBROUTINE TAB24B(SG60,DEGF,VCFC,VCFP,IFLAG)
      
      double precision SG60, DEGF, VCFC,VCFP
      integer IHYDRO, IFLAG

      DATA NBP1,NBP2,NBP3,NBP4 /7710,7885,8400,10760/
      DATA IBP1,IBP2 /7790,8250/
      DATA ITMP1,ITMP2,ITMP3 /2000,2500,3000/
      DATA IBAS /600/
      DATA IEP1,IEP2,IEP3/1500,2000,2500/
      DATA IFSG/6535/
      DATA IBT1/0/
      
      DATA K0F, K1F /1038720, 2701/
      DATA K0J, K1J /3303010, 0/
      DATA K0T, K1T /14890670,-186840/
      DATA K0G, K1G /1924571, 2438/
      
      

      VCFC=-1.0000
      VCFP=-1.0000
      IFLAG=-1
      
      ISG=(SG60*100000.0 + 25.0)/ 50.0
      ISG=ISG*5
      SG60=FLOAT(ISG)/10000.
      ITEMP=DEGF*10.0 + 0.5
      DEGF=FLOAT(ITEMP)/10.0
      IDT=ITEMP-IBAS
      
      IF(ISG-IFSG)10,20,20
 10   CONTINUE
      RETURN
 20   IF(ISG-NBP1)30,40,40
 30   K0=K0G
      K1=K1G
      GO TO 80
 40   IF(ISG-NBP2)50,50,60
 50   K0=K0T
      K1=K1T
      GO TO 80
 60   IF (ISG-NBP3)70,75,75
 70   K0=K0J
      K1=K1J
      GO TO 80
 75   IF(ISG-NBP4)78,78,10
 78   K0=K0F
      K1=K1F
 80   CONTINUE
      
      
      
      IF(ITEMP)90,100,100
 90   CONTINUE
      RETURN
 100  IF(ISG-IBP1)110,110,120
 110  IF(ITEMP-ITMP1)150,150,90
 120  IF(ISG-IBP2)130,130,140
 130  IF(ITEMP-ITMP2)150,150,90
 140  IF(ITEMP-ITMP3)150,150,90
 150  CONTINUE
      
      IX=ISG*1000
      IY=9990120
      CALL MPY24B(IX,IY,IDEN,10000)
      IDEN=(IDEN+5)/10
      IRHO=IDEN
      
      
      IF(K0.NE.K0T)GO TO 155
      CALL DIV24B(K0,IRHO,IRES,10000)
      IRES=IRES*10
      CALL DIV24B(IRES,IRHO,IALF1,10000)
      IALF1=(IALF1+5)/10
      IALF=(IALF1+K1+5)/10
      GO TO 158
 155  CALL ALFA24B(IRHO, K0, K1, IALF)
 158  CONTINUE
      
      
      CALL VCF24B(IALF, IDT, IVCF)
      IFLAG=0
      
      IF(ISG-IBP1)160,160,170
 160  IF (ITEMP-IEP1)200,200,165
 165  IFLAG=1
      GO TO 200
 170  IF(ISG-IBP2)180,180,190
 180  IF(ITEMP-IEP2)200,200,165
 190  IF(ITEMP-IEP3)200,200,165
 200  CONTINUE
      
      JVCF=(IVCF/1000+5)/10
      PVCF=JVCF
      PVCF=PVCF/10000.
      IF(IVCF-100000000)210,220,220
 210  CONTINUE
      
      JVCF=(IVCF/100+5)/10
      CVCF=JVCF
      CVCF=CVCF/100000.
      GO TO 250
 220  CONTINUE
      CVCF=PVCF
 250  CONTINUE
      VCFP=PVCF
      VCFC=CVCF
      RETURN
      END
      

      







      
      
      SUBROUTINE DIV24B(INUM,IDENOM,IRES,ISCALE)
      
      
      IRES1=INUM/IDENOM
      IRES2=(INUM-IRES1*IDENOM)*ISCALE/IDENOM
      
      IRES=IRES1*ISCALE+IRES2
      RETURN
      END
      
      SUBROUTINE ALFA24B(IRHO,K0,K1,IALF)
      INUM=K1*10000
      CALL DIV24B(INUM,IRHO,IALF1,10000)
      
      INUM=K0*100
      CALL DIV24B(INUM,IRHO,IALFS,10000)
      CALL DIV24B(IALFS,IRHO,IALF2,10000)
      IALF=(IALF1+IALF2+500)/1000
      RETURN
      END
      
      SUBROUTINE VCF24B(IALF,IDT,IVCF)
      
      ITERM1=IALF*IDT
      ITERM2=ITERM1/5*4
      
      CALL MPY24B(ITERM1,ITERM2,ITERM3,10000)
      IX=-ITERM1-ITERM3
      
      ISUM1=100000000+IX
      CALL MPY24B(IX,IX,ISUM2,10000)
      ISUM2=ISUM2/2
      CALL MPY24B(IX,ISUM2,ISUM3,10000)
      ISUM3=ISUM3/3
      CALL MPY24B(IX,ISUM3,ISUM4,10000)
      ISUM4=ISUM4/4
      CALL MPY24B(IX,ISUM4,ISUM5,10000)
      ISUM5=ISUM5/5
      CALL MPY24B(IX,ISUM5,ISUM6,10000)
      ISUM6=ISUM6/6
      
      IVCF=ISUM1+ISUM2+ISUM3+ISUM4+ISUM5+ISUM6
      RETURN
      END
      
      
      SUBROUTINE MPY24B(IX,IY,IZ,ISCALE)
      
      
      IU1=IX/ISCALE
      K1=ISCALE*IU1
      IV1=IX-K1
      IU2=IY/ISCALE
      K2=ISCALE*IU2
      IV2=IY-K2
      K3=IU1*IV2+IU2*IV1+IV1*IV2/ISCALE
      IZ=(K3+ISCALE/2)/ISCALE + IU1*IU2
      RETURN
      END
      

      
