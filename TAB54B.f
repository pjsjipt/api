      SUBROUTINE TAB54B(DEN15, DEGC, VCFC, VCFP, IFLAG)
      
      double precision DEN15, DEGC, VCFC, VCFP
      integer IFLAG
      DATA IBAS/1500/
      DATA IBP1,IBP2/7785,8240/
      DATA IBP3/10750/
      DATA NBP1,NBP2,NBP3,NBP4/7705,7875,8390,10750/
      DATA ITMP1,ITMP2,ITMP3/9500,12500,15000/
      DATA IEP1,IEP2,IEP3/6000,9000,12000/
      DATA IBT1/-1800/
      DATA IFDEN/6530/
      
      DATA K0F,K1F /1869696,4862/
      DATA K0J,K1J /5945418,0/
      DATA K0T,K1T/26803206,-336312/
      DATA K0G,K1G /3464228, 4388/
      
      
      IFLAG=-1
      VCFP=-1.00
      VCFC=-1.00
      
      IDEN=(DEN15*100. + 25.0) / 50.0
      IDEN=IDEN*5
      
      DEN15=FLOAT(IDEN)/10.0
      ITEMP=DEGC*1000.
      IF(ITEMP.GT.0)ITEMP=ITEMP+25
      IF(ITEMP.LT.0)ITEMP=ITEMP-25
      ITEMP=ITEMP/50*5
      
      DEGC=FLOAT(ITEMP)/100.0
      IDT=ITEMP-IBAS
      
      
      IF(IDEN-IFDEN) 10, 20, 20
 10   CONTINUE
      RETURN
 20   IF(IDEN-NBP1) 30, 40, 40
 30   K0=K0G
      K1=K1G
      GO TO 80
 40   IF(IDEN-NBP2)50,50,60
 50   K0=K0T
      K1=K1T
      GO TO 80
 60   IF (IDEN-NBP3)70,75,75
 70   K0=K0J
      K1=K1J
      GO TO 80
 75   IF(IDEN-NBP4)78,78,10
 78   K0=K0F
      K1=K1F
 80   CONTINUE

      IF(ITEMP)90,100,100
 90   CONTINUE
      RETURN
 100  IF(IDEN-IBP1)110,110,120
 110  IF(ITEMP-ITMP1)150,150,90
 120  IF(IDEN-IBP2)130,130,140
 130  IF(ITEMP-ITMP2)150,150,90
 140  IF(IDEN-IBP3)150,150,90
 150  IF(ITEMP-ITMP3)152,152,90
 152  CONTINUE
      IDEN=IDEN*10
      
      IF(K0.NE.K0T)GO TO 155
      CALL DIV54B(K0,IDEN,IRES)
      IRES=IRES*10
      CALL DIV54B(IRES,IDEN,IALF1)
      IALF1=(IALF1+5)/10
      IALF=(IALF1+K1+5)/10
      GO TO 158
 155  CALL ALFA54B(IDEN, K0, K1, IALF)
 158  CONTINUE
      
      IDEN=IDEN/10
      CALL VCF54B(IALF, IDT, IVCF)
      IFLAG=0
      
      IF(IDEN-IBP1)170,170,160
 160  IF (ITEMP-IEP1)200,200,165
 165  IFLAG=1
      GO TO 200
 170  IF(IDEN-IBP2)190,180,180
 180  IF(ITEMP-IEP2)200,200,165
 190  IF(ITEMP-IEP3)200,200,165
 200  CONTINUE
      
      JVCF=(IVCF/1000+5)/10
      PVCF=JVCF
      PVCF=PVCF/10000.
      IF(IVCF-100000000)230,240,240
 230  CONTINUE
      
      JVCF=(IVCF/100+5)/10
      CVCF=JVCF
      CVCF=CVCF/100000.
      GO TO 250
 240  CONTINUE
      CVCF=PVCF
 250  CONTINUE
      VCFP=PVCF
      VCFC=CVCF
      RETURN
      END
      

      
      
      SUBROUTINE DIV54B(INUM,IDENOM,IRES)
      
      IRES1=INUM/IDENOM
      IRES2=(INUM-IRES1*IDENOM)*10000/IDENOM
      IRES=IRES1*10000+IRES2
      RETURN
      END
      
      
      SUBROUTINE ALFA54B(IRHO,K0,K1,IALF)

      INUM=K1*10000
      CALL DIV54B(INUM,IRHO,IALF1)
      
      INUM=K0*100
      CALL DIV54B(INUM,IRHO,IALFS)
      CALL DIV54B(IALFS, IRHO, IALF2)
      IALF=(IALF1 + IALF2+500)/1000
      RETURN
      END
      
      SUBROUTINE VCF54B(IALF,IDT,IVCF)
      
      ITERM1=IALF*IDT
      ITERM1=ITERM1/10
      
      ITERM2=ITERM1/5*4
      
      CALL MPY54B(ITERM1, ITERM2, ITERM3)
      IX = -(ITERM1 + ITERM3)
      
      ISUM1=100000000+IX
      CALL MPY54B(IX,IX,ISUM2)
      ISUM2=ISUM2/2
      CALL MPY54B(IX,ISUM2, ISUM3)
      ISUM3=ISUM3/3
      CALL MPY54B(IX,ISUM3, ISUM4)
      ISUM4 = ISUM4/4
      CALL MPY54B(IX,ISUM4,ISUM5)
      ISUM5=ISUM5/5
      CALL MPY54B(IX,ISUM5,ISUM6)
      ISUM6=ISUM6/6
      IVCF=ISUM1+ISUM2+ISUM3+ISUM4+ISUM5+ISUM6
      RETURN
      END
      
      
      
      
      
      SUBROUTINE MPY54B(IX,IY,IZ)
      IU1=IX/10000
      K1=10000*IU1
      IV1=IX-K1
      IU2 = IY/10000
      K2=10000*IU2
      IV2=IY-K2
      K3 = IU1*IV2 + IU2*IV1 + IV1*IV2 / 10000

      IZ=(K3+5000)/10000 + IU1*IU2
      RETURN
      END
      
      
      
      
