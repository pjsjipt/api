      SUBROUTINE TAB53B(RHO,DEGC, IHYDRO,RHO15,IFLAG)
      
      
      DOUBLE PRECISION RHO,DEGC,RHO15
      INTEGER IHYDRO,IFLAG
      
      DATA IBASE /1500/
      DATA LIM1, LIM2 /1074982,652934/
      DATA IBP1, IBP2 /77850,82400/
      
      DATA ITMP1,ITMP2,ITMP3 /9500,12500,15000/
      DATA IEP1,IEP2,IEP3 /6000,9000,12000/
      
      DATA K0F,K1F /1869696,4862/
      DATA NLOF /83900/
      
      DATA K0J,K1J /5945418,0/
      DATA NLOJ,NHIJ /77900,83850/
      
      DATA K0G,K1G /3464228, 4388/
      DATA NHIG /77850/
      
      DATA INT1, INT2 /77050, 78750/
      DATA IA, IB /-336312, 26803206/
      
      NOUT=6
      IRHO=( (RHO*100)+25)/50
      IRHO=IRHO*50
      RHO=FLOAT(IRHO)/100.
      
      IF (DEGC+18) 1200, 10, 10
 10   IRD=25
      IF(DEGC) 15, 20, 20
 15   IRD=-25
      
 20   IT=((DEGC*1000)+IRD)/50
      IT=IT*5
      DEGC=FLOAT(IT)/100.
      
      IF (IRHO-65300) 1200, 40, 40
 40   IF(IRHO-107500) 50, 50, 1200
 50   IF(IRHO-IBP1)60,60,70
 60   IF(IT-ITMP1)100,100,1200
 70   IF (IRHO-IBP2)80,80,90
 80   IF(IT-ITMP2)100,100,1200
 90   IF(IT-ITMP3)100,100,1200
 100  CONTINUE
      
      IFLAG=0
      IDT=IT-IBASE
      
      IRHOT=IRHO
      
      IF (IHYDRO)101,101,112
      
 101  IRD=5
      IF(IDT)105,110,110
 105  IRD=-5
 110  IH1=(2300*IDT+IRD)/10
      IH2=(2*IDT*IDT+500)/1000
      IHYC=1000000000-IH1-IH2
      IRD=IRHO*10
      CALL MPY53B(IRD, IHYC, IRHOT,10000)
      IRHOT=(IRHOT+50)/100
      
 112  ICOUNT=0
      IF (IRHO-NHIG) 115, 115, 120
 115  K0=K0G
      K1=K1G
      IREG=0
      GO TO 150
      
 120  IF (IRHO-NHIJ) 125, 125, 130
 125  K0=K0J
      K1=K1J
      IREG=1
      GO TO 150
 130  CONTINUE
 135  IF(ICOUNT-2)140,135,135
 140  CONTINUE
      
      K0=K0F
      K1=K1F
      IREG=2


 150  ICOUNT=ICOUNT + 1
      IF (ICOUNT - 2) 160, 160, 400
           
 160  JRHOT=IRHOT*10000
      IRHO15=IRHOT
      KRHO=0
      NP=0
 300  NP=NP+1
      CALL ALF53B(IRHO15,K0,K1,IALF)
      CALL VCF53B(IALF,IDT,IVCF)
      IVCF=(IVCF+50)/100
      CALL DIV53B(JRHOT,IVCF,IRHO15,1000)
      IF(IABS(IRHO15-KRHO)-50)500, 320, 320
 320  KRHO=IRHO15
      IRHO15=(IRHO15+5)/10
      IF(NP-20) 300, 1150, 1150
      
 400  KRHO=0
      NP=0
      ICOUNT=3
      IRHO15=77884
      JRHOT=IRHOT*10000
 440  NP=NP+1
      CALL DIV53B(IB, IRHO15, IRES, 10000)
      IRES=IRES*10
      CALL DIV53B(IRES, IRHO15, IRES2, 10000)
      IRES2=(IRES2+5)/10
      IALF=(IA+IRES2+5)/10
      CALL VCF53B(IALF, IDT, IVCF)
      IVCF=(IVCF+50)/100
      CALL DIV53B(JRHOT, IVCF, IRHO15, 1000)
      
      IF (IABS(IRHO15 - KRHO)-70)500, 460, 460
      
 460  KSAV = KRHO
      KRHO = IRHO15
      IRHO15=(IRHO15 + 5)/10
      IF (NP-40) 440, 480, 480
      
      
 480  IRHO15=(KRHO + KSAV)/2
 500  IF(IRHO15 - LIM1) 510, 510, 1200
 510  IF (IRHO15 - LIM2) 1200, 520, 520
 520  JRHOT=(IRHO15+50)/100
      IRHO15=(IRHO15+5)/10
      RHO15=FLOAT(JRHOT)/10.
      IF(ICOUNT-3) 600, 720, 720

 600  IF (IREG-1) 610, 620, 640
 610  IF (IRHO15-NHIG) 680, 680, 125
 620  IF (IRHO15-NHIJ) 630, 680, 130
 630  IF(IRHO15-NLOJ) 115, 680, 680
 640  IF (IRHO15-NLOF) 125, 680, 680
 680  CONTINUE
      IF (IRHO15-INT1) 720, 400, 690
 690  IF (IRHO15-INT2) 400, 400, 720
 720  IF(IRHO-IBP1)780,780,790
      
 780  IF(IT-IEP1)820,820,830
 790  IF(IRHO-IBP2)800,800,810
      
 800  IF(IT-IEP2)820,820,830
 810  IF(IT-IEP3)820,820,830
 820  CONTINUE
      RETURN
      
 830  IFLAG = 1
      RETURN
      
      
 1150 WRITE(NOUT,*)DEGC, RHO
c 6020 FORMAT(5H1 AT ,F6.2,15H DEGREES C AND ,F6.1,  70H KG/CU METRE A)
 1200 IFLAG=-1
      RHO15=-999.9
      RETURN
      END
      
      
      SUBROUTINE DIV53B(INUM,IDENOM,IRES,ISCALE)
      
      
      IRES1=INUM/IDENOM
      IRES2=(INUM-IRES1*IDENOM)*ISCALE/IDENOM
      IRES=IRES1*ISCALE+IRES2
      RETURN
      END
      
      
      SUBROUTINE ALF53B(IRHO,K0,K1,IALF)
      
      INUM=K1*10000
      CALL DIV53B(INUM,IRHO,IALF1,10000)
      
      INUM=K0*100
      CALL DIV53B(INUM,IRHO,IALFS,10000)
      CALL DIV53B(IALFS,IRHO,IALF2,10000)
      IALF=(IALF1+IALF2+500)/1000
      RETURN
      END
      
      SUBROUTINE VCF53B(IALF,IDT,IVCF)
      
      ITERM1=IALF*IDT
      ITERM2=ITERM1/5*4
      
      CALL MPY53B(ITERM1,ITERM2,ITERM3,10000)
      ITERM3=ITERM1 + (ITERM3+5)/10
      IX=-ITERM3/10
      
      ISUM1=100000000+IX
      CALL MPY53B(IX,IX,ISUM2,1000)
      ISUM2=((ISUM2+50)/100)/2
      CALL MPY53B(IX,ISUM2,ISUM3,1000)
      ISUM3=((ISUM3+50)/100)/3
      CALL MPY53B(IX,ISUM3,ISUM4,1000)
      ISUM4=((ISUM4+50)/100)/4
      CALL MPY53B(IX,ISUM4,ISUM5,1000)
      ISUM5=((ISUM5+50)/100)/5
      CALL MPY53B(IX,ISUM5,ISUM6,1000)
      ISUM6=((ISUM6+50)/100)/6
      
      IVCF=ISUM1+ISUM2+ISUM3+ISUM4+ISUM5+ISUM6
      RETURN
      END
      
      
      SUBROUTINE MPY53B(IX,IY,IZ,ISCALE)
      
      
      IU1=IX/ISCALE
      K1=ISCALE*IU1
      IV1=IX-K1
      IU2=IY/ISCALE
      K2=ISCALE*IU2
      IV2=IY-K2
      K3=IU1*IV2+IU2*IV1+IV1*IV2/ISCALE
      IZ=(K3+ISCALE/2)/ISCALE + IU1*IU2
      RETURN
      END    
      
      
