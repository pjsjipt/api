      SUBROUTINE TAB54D(DEN15,DEGC,VCFC,VCFP,IFLAG)
      
      double precision DEN15, DEGC, VCFC,VCFP
      integer IHYDRO, IFLAG

      DATA IBAS /1500/
      DATA IBP1,IBP2/7785,8240/
      DATA NBP1 /11640/
      DATA ITMP1/15000/
      DATA IFDEN/8000/
      DATA IBT1/-2000/
      DATA K0,K1/0,62780/

      VCFC=-1.0000
      VCFP=-1.0000
      IFLAG=-1
      
      IDEN=(DEN15*100+25.0)/50.0
      IDEN=IDEN*5
      DEN15=FLOAT(IDEN) / 10.0
      ITEMP=DEGC*1000.
      IF(ITEMP.GT.0)ITEMP=ITEMP+25
      IF(ITEMP.LT.0)ITEMP=ITEMP-25
      ITEMP=ITEMP/50*5
      DEGC=FLOAT(ITEMP)/100.0
      
      IF(IDEN-IFDEN)10,20,20
 10   CONTINUE
      RETURN
 20   IF(IDEN-NBP1)30,30,10
 30   CONTINUE
      
      IDT=ITEMP-IBAS
      IF(ITEMP-IBT1)40,60,60
      
 40   CONTINUE
      RETURN
 60   IF(ITEMP-ITMP1)100,100,40
 100  CONTINUE
      
      IDEN=IDEN*10
      CALL ALFA54D(IDEN, K0, K1, IALF)
      
      CALL VCF54D(IALF, IDT, IVCF)
      IFLAG=0
      
      
      JVCF=(IVCF/1000+5)/10
      PVCF=JVCF
      PVCF=PVCF/10000.
      IF(IVCF-100000000)230,240,240
 230  CONTINUE
      
      JVCF=(IVCF/100+5)/10
      CVCF=JVCF
      CVCF=CVCF/100000.
      GO TO 250
 240  CONTINUE
      CVCF=PVCF
 250  CONTINUE
      VCFP=PVCF
      VCFC=CVCF
      RETURN
      END
      

      

      
      SUBROUTINE DIV54D(INUM,IDENOM,IRES,ISCALE)
      
      
      IRES1=INUM/IDENOM
      IRES2=(INUM-IRES1*IDENOM)*ISCALE/IDENOM
      
      IRES=IRES1*ISCALE+IRES2
      RETURN
      END
      
      SUBROUTINE ALFA54D(IRHO,K0,K1,IALF)
      INUM=K1*1000
      CALL DIV54D(INUM,IRHO,IALF1,10000)
      
      INUM=K0*100
      CALL DIV54D(INUM,IRHO,IALFS,10000)
      CALL DIV54D(IALFS,IRHO,IALF2,10000)
      IALF=(IALF1+IALF2+500)/1000
      RETURN
      END
      
      SUBROUTINE VCF54D(IALF,IDT,IVCF)
      
      ITERM1=IALF*IDT
      ITERM1=ITERM1/10
      ITERM2=ITERM1/5*4
      
      CALL MPY54D(ITERM1,ITERM2,ITERM3,10000)
      IX=-ITERM1-ITERM3
      
      ISUM1=100000000+IX
      CALL MPY54D(IX,IX,ISUM2,10000)
      ISUM2=ISUM2/2
      CALL MPY54D(IX,ISUM2,ISUM3,10000)
      ISUM3=ISUM3/3
      CALL MPY54D(IX,ISUM3,ISUM4,10000)
      ISUM4=ISUM4/4
      CALL MPY54D(IX,ISUM4,ISUM5,10000)
      ISUM5=ISUM5/5
      CALL MPY54D(IX,ISUM5,ISUM6,10000)
      ISUM6=ISUM6/6
      
      IVCF=ISUM1+ISUM2+ISUM3+ISUM4+ISUM5+ISUM6
      RETURN
      END
      
      
      SUBROUTINE MPY54D(IX,IY,IZ,ISCALE)
      
      
      IU1=IX/ISCALE
      K1=ISCALE*IU1
      IV1=IX-K1
      IU2=IY/ISCALE
      K2=ISCALE*IU2
      IV2=IY-K2
      K3=IU1*IV2+IU2*IV1+IV1*IV2/ISCALE
      IZ=(K3+ISCALE/2)/ISCALE + IU1*IU2
      RETURN
      END
      

      
