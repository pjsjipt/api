      SUBROUTINE RHO6A(IAPI,IRHO)
      
      IDENOM=IAPI+1315
      IRHO=(1413601980/IDENOM+5)/10
      RETURN
      END
      
      SUBROUTINE DIV6A(INUM,IDENOM,IRES,ISCALE)
      
      
      IRES1=INUM/IDENOM
      IRES2=(INUM-IRES1*IDENOM)*ISCALE/IDENOM
      
      IRES=IRES1*ISCALE+IRES2
      RETURN
      END
      
      SUBROUTINE ALFA6A(IRHO,K0,K1,IALF)
      INUM=K1*10000
      CALL DIV6A(INUM,IRHO,IALF1,10000)
      
      INUM=K0*100
      CALL DIV6A(INUM,IRHO,IALFS,10000)
      CALL DIV6A(IALFS,IRHO,IALF2,10000)
      IALF=(IALF1+IALF2+500)/1000
      RETURN
      END
      
      SUBROUTINE VCF6A(IALF,IDT,IVCF)
      
      ITERM1=IALF*IDT
      ITERM2=ITERM1/5*4
      
      CALL MPY6A(ITERM1,ITERM2,ITERM3)
      IX=-ITERM1-ITERM3
      
      ISUM1=100000000+IX
      CALL MPY6A(IX,IX,ISUM2)
      ISUM2=ISUM2/2
      CALL MPY6A(IX,ISUM2,ISUM3)
      ISUM3=ISUM3/3
      CALL MPY6A(IX,ISUM3,ISUM4)
      ISUM4=ISUM4/4
      CALL MPY6A(IX,ISUM4,ISUM5)
      ISUM5=ISUM5/5
      CALL MPY6A(IX,ISUM5,ISUM6)
      ISUM6=ISUM6/6
      
      IVCF=ISUM1+ISUM2+ISUM3+ISUM4+ISUM5+ISUM6
      RETURN
      END
      
      
      SUBROUTINE MPY6A(IX,IY,IZ)
      
      ISCALE=10000
      
      IU1=IX/ISCALE
      K1=ISCALE*IU1
      IV1=IX-K1
      IU2=IY/ISCALE
      K2=ISCALE*IU2
      IV2=IY-K2
      K3=IU1*IV2+IU2*IV1+IV1*IV2/ISCALE
      IZ=(K3+ISCALE/2)/ISCALE + IU1*IU2
      RETURN
      END
      

      
